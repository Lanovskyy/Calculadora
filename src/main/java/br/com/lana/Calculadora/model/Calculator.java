package br.com.lana.Calculadora.model;

public class Calculator {

    //adicionaremos somente o m√©todo de soma


    public static float sum(float n1, float n2){

        return n1 + n2;
    }

    public static float minus(float n1, float n2){

        return n1 - n2;
    }

    public static float divi(float n1, float n2){

        return n1 / n2;
    }

    public static float multi(float n1, float n2){

        return n1 * n2;
    }
}
